package vsp.dataObject;

public class Investment {
  double capital = 0.0;
  float shares = 0f;
  public double getCapital() {
    return capital;
  }
  public void setCapital(double capital) {
    this.capital = capital;
  }
  public float getShares() {
    return shares;
  }
  public void setShares(float shares) {
    this.shares = shares;
  }

}
